    private void AddManufacturerADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddManufacturerADDActionPerformed
        String mname = name.getText();
        String mAddress = address.getText();
        String mEmail = email.getText();
        String telephoneNumber = phoneNumber.getText();
        
        if(mname.isEmpty() || mAddress.isEmpty() || mEmail.isEmpty()){
            JOptionPane.showMessageDialog(null, "This field cannot be empty");
        }
        else if(!isEmail(mEmail)){
            JOptionPane.showMessageDialog(null, "Please input an valid email address");
            email.setText(null);
        }
        else if(telephoneNumber.length() != 10){
            JOptionPane.showMessageDialog(null, "Please enter a valid telephone number");
            phoneNumber.setText(null);
            
        }
        else{
            try {
                String query = "INSERT INTO manufacture(name, address, email) VALUES(?,?,?)";
                ps = connection.prepareStatement(query);
                ps.setString(1, mname);
                ps.setString(2, mAddress);
                ps.setString(3, mEmail);
                
                ps.executeUpdate();
                
                int maID = getManufactureID(mname);
                
                query = "INSERT INTO manufacture_phone(maID, phone) VALUES(?,?)";
                ps = connection.prepareStatement(query);
                ps.setInt(1, maID);
                ps.setString(2, telephoneNumber);
                
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Insertion Successful");
                
                ManufacturerUI ui = new ManufacturerUI();
                ui.setVisible(true);
                this.dispose();
              
                
            } catch (SQLException ex) {
                Logger.getLogger(AddManufacturer.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
        
        
    }//GEN-LAST:event_AddManufacturerADDActionPerformed

