    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        if(selectedWID != null){
            
            //Asking 
        String code = JOptionPane.showInputDialog(
        null, 
        "This action cannot be undone.\nThis will permanently delete the '" +selectedWName+ "' warehouse instance\n\nPlease type in the name of the warehouse to confirm.", 
        "Are you absolutely sure? ", 
        JOptionPane.WARNING_MESSAGE
    );
        if(code.equals(selectedWName)){
        
        String sqlExec = " DELETE FROM warehouse WHERE wID = '"+selectedWID+"'";
        try{
        ps=connection.prepareStatement(sqlExec);
        ps.execute();
        JOptionPane.showMessageDialog(this, "Warehouse '"+selectedWName+"' Deleted");
        selectedWName=null;
        refreshTable();
        
        }catch(Exception e){
        e.printStackTrace();
        }
        }else{
        JOptionPane.showMessageDialog(this, "Invalid Warehouse Name");
        }
           
            
        
        }else{
            JOptionPane.showMessageDialog(null, "No Selected Item");
            
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

