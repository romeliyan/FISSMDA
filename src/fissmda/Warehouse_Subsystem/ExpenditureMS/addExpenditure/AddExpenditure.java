     public void fillCombo() {

        try {

            String sqlExec = "select * from warehouse";
            ps = con.prepareStatement(sqlExec);
            rs = ps.executeQuery();

            while (rs.next()) {
                String getWarehouseName = rs.getString("warehouse_name");
                WarehouseListCombo.addItem(getWarehouseName);
                
                
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this,"Error with Fetch Data");
            e.printStackTrace();

        }
    }

