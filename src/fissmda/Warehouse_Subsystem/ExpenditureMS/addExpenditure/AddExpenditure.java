    private void addExpenditureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addExpenditureActionPerformed

        if(!(billAmount.getText().equals("")||selectBill.getSelectedItem().toString().equals("")||WarehouseListCombo.getSelectedItem().toString().equals("")||billStartDate.getDate()==null||billEndDate.getDate()==null)){
       if(billAmount.getText().toString().matches("[0-9]+")){ 
        String amount = billAmount.getText().toString();
        String billType =selectBill.getSelectedItem().toString();
        String selectedWarehouse = WarehouseListCombo.getSelectedItem().toString();
        
        
        
        ExpenditureHandler validE = new ExpenditureHandler(selectedWarehouse,billType,amount);
        
      
                String sqlExc = "select wID from warehouse where warehouse_name = '"+selectedWarehouse+"'";
        
        try{
            ps = con.prepareStatement(sqlExc);
            rs = ps.executeQuery();
            
            //check date field field is null
            
           
            Date stDate = new Date(billStartDate.getDate().getTime());
            Date endDate = new Date(billEndDate.getDate().getTime());
            
            int wID=0;
            while(rs.next()){
            
                
                 wID = rs.getInt(1);
                System.out.println("  "+wID);
            } 
            
            
        
            String sqlExec ="Insert into expenditure (amount,category,arrivalDate,deadLine,warehouse_WID) values(?,?,?,?,?)";
            
            
             int choice = JOptionPane.showConfirmDialog(this, "Do You want to Add to Database");
                            if (choice == 0) {
                                try {
                                    ps = con.prepareStatement(sqlExec);
                                    ps.setString(1, amount);
                                    ps.setString(2,billType);
                                    ps.setDate(3,stDate);
                                    ps.setDate(4,endDate);
                                    ps.setInt(5, wID);
                                    ps.execute();
                                    JOptionPane.showMessageDialog(this, "Successfully Added to Database");
                                } catch (Exception e) {
                                    e.printStackTrace();
                                }
                            }
         
        }catch(Exception e){
            e.printStackTrace();
        }
       }else{
           JOptionPane.showMessageDialog(this, "Amount Field should numerical value");
       }
        }else{
        
             JOptionPane.showMessageDialog(this, "Al the the fields should be completed");

        }
    }//GEN-LAST:event_addExpenditureActionPerformed

