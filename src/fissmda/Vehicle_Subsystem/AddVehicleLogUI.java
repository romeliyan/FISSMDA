    public void fillVehicleCombobox()
    {
        try {
            String q = "SELECT * FROM vehicle";
            ps = connection.prepareStatement(q);
            rs = ps.executeQuery(q);
            
            //remove all available items 
            selectvehiclecombo5.removeAllItems();
            selectvehiclecombo5.addItem("Select Vehicle");
            
            while(rs.next()){
                
                selectvehiclecombo5.addItem(rs.getString("vehicle_number") );
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RemoveVehicleUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
    public boolean isInt (String amount1)
    {
        try
        {
            Integer.parseInt(amount1);
        }
        catch(NumberFormatException e)
            
        {
            return false;
        }
        catch(NullPointerException e)
        {
            return false;
        }
        return true;
    }
    
    public int VehicleID1(String vehicle_number)
    {
        int vehicleId = 0;
        
        
        String vID = "SELECT vID FROM vehicle WHERE vehicle_number = ?";
        
        try
        {
            ps = connection.prepareStatement(vID);
            ps.setString(1, vehicle_number);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {
                vehicleId = (int) rs.getInt("vID");
            }
            return vehicleId;
        }
        
        catch (SQLException ex) {
            Logger.getLogger(RemoveVehicleUI.class.getName()).log(Level.SEVERE, null, ex);
            return vehicleId;
        }
        
    }

