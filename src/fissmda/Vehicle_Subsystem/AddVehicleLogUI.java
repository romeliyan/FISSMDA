    private void addbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbuttonActionPerformed

        //Connecting to DataBase
        String getVehicleItem = selectvehiclecombo5.getSelectedItem().toString();
        String getExpenditure = selectexpanditurecombo.getSelectedItem().toString();
        String Amount = amounttextfield.getText();
        
        int amount1;
        
        if(getVehicleItem.equals("Select Vehicle"))
        {
            JOptionPane.showMessageDialog(null,"Please select a Vehicle");
        }
        else if(getExpenditure.equals("Select Expenditure"))
        {
            JOptionPane.showMessageDialog(null,"Please Select a Expenditure to Add");
        }
        else if(Amount.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Amount Can not be Empty");
        }
        else if(!isInt(Amount))
        
        {
            JOptionPane.showMessageDialog(null,"Amount Should be an Integer Value. Please Enter Valid Value");
            amounttextfield.setText(null);
        }
        else
        {
            int vehicleId = VehicleID1(getVehicleItem);
            
            try {
            String q1 = "INSERT INTO vehicle_log (vID,amount,expenditure) values (?,?,?)";
            
            int amount2 = Integer.parseInt(Amount);
         
            ps = connection.prepareStatement(q1);
            
            ps.setInt(1, vehicleId);
            ps.setInt(2, amount2);
            ps.setString(3, getExpenditure);
            
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Successful");

        
        }
        catch (SQLException ex) {
            Logger.getLogger(AddVehicleUI.class.getName()).log(Level.SEVERE, null, ex);
            
        }
            
        }
        
        amounttextfield.setText(null);
        
        
        

        
        
        
    }//GEN-LAST:event_addbuttonActionPerformed

