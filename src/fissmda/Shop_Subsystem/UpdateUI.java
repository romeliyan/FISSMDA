    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try{
            String sql = "SELECT sID, name, sOwner, address, email, phone, latitude, longitude FROM shop WHERE name = ?";
            Connection con = DBConnection.getConnection();
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1,searchSName.getText());
            
            ResultSet rs;
            
            rs = ps.executeQuery();
            
            if(rs.next()){
                String ShpID = rs.getString("sID");
                int ShopId = Integer.parseInt(ShpID);
                shopID.setText(ShpID);
                String shopName = rs.getString("name");
                sName.setText(shopName);
                String SO = rs.getString("sOwner");
                oName.setText(SO);
                String Email = rs.getString("email");
                email.setText(Email);
                String ad = rs.getString("address");
                address.setText(ad);
                String pn = rs.getString("phone");
                pNumber.setText(pn);
                String Latitude1 = rs.getString("latitude");
                float Latitude2 = Float.parseFloat(Latitude1);
                latitude.setText(Latitude1);
                String Longitude1 = rs.getString("longitude");
                float Longitude2 = Float.parseFloat(Longitude1);
                longitude.setText(Longitude1);
                
            }
            else{
                JOptionPane.showMessageDialog(this, "The Shop you searched is not in the system.");       
            }
            
        } catch (SQLException e) {
            Logger.getLogger(UpdateUI.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try {
            String Email = email.getText();
            String phoneNumber1 = pNumber.getText();
              if(sName.equals("")){
                JOptionPane.showMessageDialog(this, "Shop Name Field Cannot be Empty");   
                return;
            }
            
            if(oName.equals("")){
                JOptionPane.showMessageDialog(this, "Owner Name Field Cannot be Empty");
                return;
            }
            
            if(address.equals("")){
                JOptionPane.showMessageDialog(this, "Address Field Cannot be Empty");
                return;
            }
           
            if(pNumber.equals("") ){
                JOptionPane.showMessageDialog(this, "Phone Number Field Cannot be Empty");
                return;
            }
            if(((phoneNumber1.matches("^[0-9]{10}$") != true)) ){
                JOptionPane.showMessageDialog(this, "Phone Number Should Include 10 Numerical Values Only"); 
                return;
            }

            if(Email.equals("")){
                JOptionPane.showMessageDialog(this, "Email Field Cannot be Empty");  
                return;
            }
            if(latitude.equals("")){
                JOptionPane.showMessageDialog(this, "Latitude Field Cannot be Empty"); 
                return;
            }
            if(longitude.equals("")){
                JOptionPane.showMessageDialog(this, "Longitude Field Cannot be Empty"); 
                return;
            }
          
             // a string without a "@" is an invalid email address
            // a string without a "."  is an invalid email address
            if ((Email.indexOf("@")  < 0)  || (Email.indexOf(".") < 0 ) ){
                JOptionPane.showMessageDialog(this, "Please enter a valid email address"); 
                return;
            }
            if( (Float.parseFloat(latitude.getText()) < -90) || ((Float.parseFloat(latitude.getText()) > 90))){
                JOptionPane.showMessageDialog(this, "Latitude value must be number between -90 to 90"); 
                return;
            }  
            if( (Float.parseFloat(longitude.getText()) < -180) || ((Float.parseFloat(latitude.getText()) > 180))){
                JOptionPane.showMessageDialog(this, "Longitude value must be number between -180 to 180");  
                return;
            }
            
            
            Connection con = DBConnection.getConnection();
            String sql = "update shop set name = ?, address= ?, phone= ?, email= ?, longitude = ?, latitude  = ? where name = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, sName.getText());
            ps.setString(2, address.getText());
            ps.setString(3, pNumber.getText());
            ps.setString(4, email.getText());
            ps.setString(5, longitude.getText());
            ps.setString(6, latitude.getText());
            ps.setString(7, searchSName.getText());
            ps.execute();
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure to update this shop in the database");
               if(dialogResult == JOptionPane.YES_OPTION){
                   ps.execute();
                   JOptionPane.showMessageDialog(this, "Shop details updated successfully");       
               }else{
                   return;
               }




        } catch (SQLException ex) {
            Logger.getLogger(UpdateUI.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

